const suma = function (num1, num2) {
  let result = num1 + num2;
  return result;
}

const resta  = function (num1, num2) {
  let result = num1 - num2;
  return result;
}

const mult = function (num1, num2) {
  let result = num1 * num2;
  return result;
}

const div = function (num1, num2) {
  let result = num1 / num2;
  return result;
}


export {
  suma,
  resta,
  mult,
  div
}
